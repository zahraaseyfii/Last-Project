package com.example.lastproject;

public class Income {
    private String mablagh,daste,hesab,date,note;

    public Income(){

    }

    public String getMablagh() {
        return mablagh;
    }

    public void setMablagh(String mablagh) {
        this.mablagh = mablagh;
    }

    public String getDaste() {
        return daste;
    }

    public void setDaste(String daste) {
        this.daste = daste;
    }

    public String getHesab() {
        return hesab;
    }

    public void setHesab(String hesab) {
        this.hesab = hesab;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }
}
